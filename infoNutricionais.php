<h1>O que são Calorias?</h1>
<p>Calorias podem ser traduzidas como toda a energia que o organismo
	absorve e gasta diariamente. São medidas de calor dos alimentos, o
	combustível do organismo, e elas são ingeridas através do que você
	come; como os carboidratos (pães, massas, legumes, frutas e verduras).
	Estas são as primeiras a serem queimadas pelo corpo como fontes de
	energia, por isso são chamadas de CALORIAS RÁPIDAS. A segunda fonte de
	calorias é as gorduras e proteínas (carnes, óleos, vegetais, leites,
	queijos e ovos). Apesar de produzir mais energia, é a segunda fonte e a
	que proporciona as reservas energéticas para o corpo humano. É
	classificada como CALORIAS LENTAS porque sua digestão é um pouco mais
	demorada. A terceira fonte, que oferece CALORIAS VAZIAS, porque não
	traz nenhum benefício ao organismo nem fornece qualquer nutriente, são
	as bebidas alcoólicas, os refrigerantes e os doces.</p>
<p>OBS: Elimine, portanto, as calorias vazias que não trazem benefícios
	para o seu organismo e só fazem engordar.</p>

