        <h1> Aprenda como preprar caldos e pastéis</h1>

<h3>Caldo de Frango</h3>
<address><em>Ingredientes:</em></address>
<ul>
  <li>1 kg de mandioca cozida e batida no liquidificador</li>
  <li>1 kg de peito de frango cozido e desfiado</li>
  <li>2 colheres de sopa de óleo</li>
  <li>Tempero a gosto cebolinha e cheiro verde a gosto</li>
  <li>2 caldos de galinha</li>
</ul>

<address><em>Ingredientes:</em></address>

<ul>
  <li>Em uma panela coloque o óleo, a mandioca, o tempero.</li>
  <li>Deixe ferver, logo em seguida coloque o peito, o caldo
de galinha dissolvido em 2 xícaras de água, deixe ferver
mais um pouco e é só servir com o cheiro verde à vontade</li>

</ul>

<h3>Caldo de Feijão</h3>
<address><em>Ingredientes:</em></address>
<ul>
  <li>1 kg de feijão de sua preferência já cozido</li>
  <li>1 kg de bacon </li>
  <li>2 cebolas grandes cortadas </li>
  <li>1 maço de cheiro verde picados (cebolinha)</li>
  <li>4 dentes de alho amassados</li>
  <li>2 tabletes de caldo de bacon</li>
  <li>2 pimentas de cheiro</li>
  <li>100 ml de óleo de soja (pode ser o óleo que fritou o bacon e a lingüiça)</li>
  <li>200 ml de água ou mais</li>
</ul>

<address><em>Modo de Preparo:</em></address>

<ul>
  <li>Corte em cubinhos o bacon em pedacinhos pequenos</li>
  <li>Leve ao fogo para fritar primeiro o bacon depois a lingüiça e em seguida deixe escorrer</li>
  <li>Bata no liquidificador, o feijão, as cebolas, o cheiro verde e as pimentas de cheiro, até virar um
      líquido pastoso</li>
  <li>Em uma panela coloque o óleo e os dentes de alho amassados, deixe dourar</li>
  <li>Acrescente o feijão já batido e os tabletes de caldo de bacon, coloque a água e deixe ferver até
      ficar o caldo com a consistência de sua preferência</li>
  <li>Colocar o bacon dentro do caldo quando estiver fervendo</li>
  <li>Está pronto e só servir com torradas e queijo ralado</li>
</ul>



<h3>Pastel de Carne</h3>
<address><em>Ingredientes:</em></address>
<ul>
  <li>250 g de carne moída</li>
  <li>1 cebola </li>
  <li>1 dente de alho</li>
  <li>1 colher de sopa de manteiga</li>
  <li>Sal</li>
  <li>21 pitada de pimenta do reino</li>

</ul>


<address><em>Modo de Preparo:</em></address>

<ul>
  <li>Coloque a carne em uma panela e leve-a ao fogo, vá mexendo até que fique completamente enxuta e solta;</li>
  <li>Depois, junte a manteiga, a cebola batidinha e o alho socado;</li>
  <li>Refogue bem, tempere com sal e pimenta-do-reino, e boa porção de salsa e cebolinha verde cortadas bem fininha;</li>
  <li>Deixe por mais uns 2 a 3 minutos no fogo e leve para esfriar;</li>
  <li>Ponha uma colher do recheio no meio do círculo de massa, dobre-o ao meio e com um garfo aperte a emenda
para evitar que o pastel se abra ao fritar.</li>
  <li>Frite os pasteis, em uma panela larga e funda ponha o óleo para aquecer, deixe esquentar bem e só então
coloque os pasteis um a um com cuidado com auxilio de uma escumadeira para evitar queimaduras. Lembre-se que eles
incham e ocupam mais espaço na panela ao serem fritos. Eles estarão prontos quando estiverem douradinhos.
Deixe escorrer em um prato com papeis toalha. Sirva quente.</li>

</ul>


<h3>Pastel de Queijo</h3>
<address><em>Ingredientes:</em></address>
<ul>
  <li>500 g de queijo curado ou fresco</li>
  <li>½ xícara de cebolinha bem fininha cebola</li>
  <li>1 colher de café de pimenta de reino (de preferência moída na hora)</li>

</ul>


<address><em>Modo de Preparo:</em></address>

<ul>
  <li>Corte o queijo em pedaços médios e coloque em uma tigela. Com a ajuda de um garfo amasse todo o queijo
até fiar bem picadinho. Junte a cebolinha picadinha e a pimenta moída e misture bem. Reserve.</li>
  <li>Ponha uma colher do recheio no meio do círculo de massa, dobre-o ao meio e com um garfo aperte a
emenda para evitar que o pastel se abra ao fritar.</li>
  <li>Frite os pasteis, em uma panela larga e funda ponha o óleo para aquecer, deixe esquentar bem e só
então coloque os pasteis um a um com cuidado com auxilio de uma escumadeira para evitar queimaduras.
Lembre-se que eles incham e ocupam mais espaço na panela ao serem fritos. Eles estarão prontos quando estiverem douradinhos.
Deixe escorrer em um prato com papeis toalha. Sirva quente.</li>
</ul>